    private void listCars(){
        
        DefaultTableModel model = ( DefaultTableModel ) jTCarros.getModel();
        jTCarros.setRowSorter( new TableRowSorter(model));
        model.setRowCount(0);
        
        ResponseStatus<List<CarEntity>> response = this.carController.list();
        
        System.out.println(response.message);
        
        if(response.status())
        {
           this.cars = response.data;
           
           for(CarEntity carEntity : this.cars)
           {
                model.addRow( new Object[]{
                    carEntity.carCode,
                    carEntity.manufacturerEntity.manufactureName,
                    carEntity.modelEntity.modelName,
                    carEntity.carPlaque,
                    carEntity.carYear,
                    carEntity.carChassi
                });
           }
        }
    }


    private void jBDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDeleteActionPerformed
        // TODO add your handling code here:
        
        int index = jTCarros.getSelectedRow();
        
        CarEntity carEntity = this.cars.get(index);
        
        int showConfirmDialog = JOptionPane.showConfirmDialog(
            this,
            "Você realmente desejá excluir o carro :",
            "Excluir carro",
            JOptionPane.OK_CANCEL_OPTION
        );
        
        if(showConfirmDialog==0)
        {
            ResponseStatus<CarEntity> response = this.carController.delete(carEntity);
            
            JOptionPane.showConfirmDialog(
                this,
                response.message,
                "INFO",
                JOptionPane.CLOSED_OPTION
            );
            
            this.jBDelete.setEnabled(false);
            this.jBEdit.setEnabled(false);
            
            this.listCars();
        }
        
    }//GEN-LAST:event_jBDeleteActionPerformed

